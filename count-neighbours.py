def count_neighbours(grid, row, col):
    result = 0
    for EachRow in range(row-1, row+2):
        if EachRow < 0 or EachRow >= len(grid): 
            continue
        for EachCol in range(col-1, col+2):
            if EachCol < 0 or EachCol >= len(grid[0]):
                continue
            if(grid[EachRow][EachCol] == 1 and not(EachRow==row and EachCol==col)):
                result += 1
    return result


